nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade: "))

# IF e ELSE simples
if idade >= 18:
    print(f"Olá {nome}, você é MAIOR de idade.")
else:
    print(f"Olá {nome}, você é MENOR de idade.")



